    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        supportRequestWindowFeature(Window.FEATURE_NO_TITLE);
        setContentView(R.layout.activity_result);

        g = (RelativeLayout) findViewById(R.id.gun);
        naditext = (TextView) findViewById(R.id.textnadi);
        gunatext = (TextView) findViewById(R.id.gunatext);
        longtext = (TextView) findViewById(R.id.longtext);
        imgg = (ImageView) findViewById(R.id.imgg);



        naditext.setTypeface(Typeface.createFromAsset(getAssets(),"fonts/hindi.ttf"));

        male_index = Integer.parseInt(getIntent().getExtras().getString("male"));
        female_index = Integer.parseInt(getIntent().getExtras().getString("female"));

        if(nadi[male_index]==nadi[female_index]){
            nadi_status = 1;
        }
        else{
            nadi_status = 0;
        }

        if(male_index == female_index){
            nadi_status = 0;
        }

        if(nadi_status == 1){
            g.setVisibility(View.GONE);
            naditext.setTextColor(getResources().getColor(R.color.orange));
        }
        else{
            float guna = gunmatrix[female_index-1][male_index-1];
            int gunaint = ((int) guna);
            if(guna >= 18){
                imgg.setBackground(getResources().getDrawable(R.drawable.circle_green));
                gunatext.setTextColor(getResources().getColor(R.color.colorPrimaryDark));
                gunatext.setText(""+gunaint);
                longtext.setTextColor(getResources().getColor(R.color.green));
                longtext.setText("वर और वधू के कूल "+guna+" गूण मिल रहे है....अतः उनका विवाह हो सकता");
            }
            else{
                imgg.setBackground(getResources().getDrawable(R.drawable.circle_red));
                gunatext.setTextColor(getResources().getColor(R.color.colorPrimaryDark));
                gunatext.setText(""+gunaint);
                longtext.setTextColor(getResources().getColor(R.color.orange));
                longtext.setText("वर और वधू के सिर्फ "+guna+" गूण मिल रहे है....अतः उनका विवाह नही हो सकता");
            }
        }
    }
